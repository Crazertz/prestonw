package NBHgames;

public class Hunter extends Combatant
{
	String name;
	point position;
	public Hunter(String n, point p) 
	{
		super(n,p);
	}
	
	public void move(String input)
		{
		String w = "w";
		String e = "e";
		String s = "s";
		String q = "q";
		if(input.equals(w))
			{
				position.x += 1;
			}
		if(input.equals(e))
			{
				position.y += 1;
			}
		if(input.equals(s))
			{
				position.x -= 1;
			}
		if(input.equals(q))
			{
				position.y -= 1;
			}
		}
}
